public abstract class T01_AbstractClass {
    /**
     * 抽象类的访问修饰符体现在方法定义处,此处为default,默认为default
     */
    abstract void defaultM();

    /**
     * 抽象类的访问修饰符体现在方法定义处,此处为public
     */
    public abstract void publicM();
}
